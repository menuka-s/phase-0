def get_grade (x)
  if x >= 90
    return "A"
  elsif x >= 80
    return "B"
  elsif x >= 70
    return "C"
  elsif x >= 80
    return "D"
  elsif x >= 80
    return "F"
  else
    return "Invalid Grade"
  end
end